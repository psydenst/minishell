#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	if (n > 0)
	{
		while (n)
		{
			if (*(char *) s == (char) c)
				return ((char *) s);
			n--;
			s++;
		}	
	}
	return (0);
}
